const getRandomWordTemplate = (list: FormalWord[]) => {
  const random = Math.floor(Math.random() * list.length);
  return list[random];
};

export type FormalWord = {
  word: string;
  meaning: Record<string, string>;
  phonetics: {
    us: string;
    uk: string;
  };
  examples: {
    context: string;
    sentence: string;
    translation: string;
  }[];
  root_analysis: {
    root: string;
    meaning: string;
    derived_words: {
      word: string;
      meaning: string;
    }[];
  };
  collocations: {
    phrase: string;
    translation: string;
  }[];
  mnemonics: string;
};

